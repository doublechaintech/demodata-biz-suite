
package com.test.demodata;


public class DemodataRuntimeException extends RuntimeException {
    static final long serialVersionUID = -1;

    public DemodataRuntimeException() {
        super();
    }


    public DemodataRuntimeException(String message) {
        super(message);
    }


    public DemodataRuntimeException(String message, Throwable cause) {
        super(message, cause);
    }


    public DemodataRuntimeException(Throwable cause) {
        super(cause);
    }


   
}












